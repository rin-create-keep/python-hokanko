# ユーザー入力（複数OK）
user_input = input("都道府県名を入力してください（スペース区切りで複数可）: ")

# 入力を分割
prefs = user_input.split()

# 省略形を正規化する関数
def normalize(pref):
    for key in todouhuken.keys():
        # 入力が完全一致 or 「都」「道」「府」「県」を除いた名前が一致
        if pref == key or pref == key.replace("都", "").replace("道", "").replace("府", "").replace("県", ""):
            return key
    return None

# 入力された都道府県ごとに処理
for pref in prefs:
    normalized = normalize(pref)
    if normalized:
        print(f"{normalized}の名産品は「{todouhuken[normalized]}」です！")
    else:
        print(f"「{pref}」は登録されていない都道府県名です。")